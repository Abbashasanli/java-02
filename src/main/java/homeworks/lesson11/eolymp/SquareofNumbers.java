package homeworks.lesson11.eolymp;

import java.util.Scanner;

public class SquareofNumbers {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int a = scan.nextInt();
        for (int i = 1; i <= a; i++) {
                if (i * i <= a) {
                    System.out.print(i * i + " ");
                }else break;


                }
            }

    }

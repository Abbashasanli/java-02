package homeworks.lesson5;

public class AssignmentOperator {
    public static void main(String[] args) {
        int c=100;
        c=c+20;
        System.out.println(c);
        c=++c;
        System.out.println(c);
        c+=20;
        System.out.println(c);
        c=--c;
        System.out.println(c);


    }
}
